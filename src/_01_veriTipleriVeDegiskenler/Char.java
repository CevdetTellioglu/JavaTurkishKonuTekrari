package _01_veriTipleriVeDegiskenler;

public class Char {
    public static void main(String[] args) {

        /*
        CHAR: Java da karakterler char veri tipi içinde saklanır.
         C/C++ gibi yazılım dillerinde char veri tipi 8 bitlik büyüklüğe
         sahipken, Java’da 16 bitlik büyüklüğe sahiptir. Bunun sebebi
         Java’nın Unicode karakter setini kullanıyor olmasıdır.
         Ayrıca Java ASCII kod yapısını da desteklemektedir.
         Kod geliştirirken char anahtar kelimesi ile tanımlama yapılır.
         */

        char charDeger1 = 65;
        System.out.println(charDeger1);
        char charDeger2 = 'A';
        System.out.println(charDeger2);

        //Yukarıda tanımlanan değişkenlerin ikisi de A karakterini belirtmektedir.


    }
}
